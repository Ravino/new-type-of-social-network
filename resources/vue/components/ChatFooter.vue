methods: {
    onMessageKeyDown(ev) {
        if (13===ev.keyCode  &&  this.newMessage.trim()!=='') {
            const newMsg = {
                body: this.newMessage.trim(),
                createdAt: moment().unix(),
                isMine: true,
                isRead: false,
                isEdited: false
            };

            const evData = {
                message : newMsg,
                chatId : this.currentDialog.id
            };

